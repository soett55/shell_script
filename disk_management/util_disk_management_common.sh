## df : 디스크 남은 용량 확인
# df -k # 킬로바이트 단위로 현재 남은 용량을 확인
# df -m # 메가바이트 단위로 남은 용량을 왁인 
# df -h # 보기 좋게 보여줌
df -h . # 현재 디렉토리가 포함된 파티션의 남은 용량을 확인

## du : 현재 디렉토리에서 서브디렉토리까지 용량 확인
# du -a : 현재 디렉토리의 사용량을 파일단위 출력
# du -s : 총 사용량을 확인
# du -h : 보기 좋게 바꿔줌 
du -sh . : 한단계 서브디렉토리 기준으로 보여준다. 

